    while True:
        try:
            raw = input(f"  {B}>{RST} Choice [{default_idx+1}]: ").strip()
        except (KeyboardInterrupt, EOFError):
            print(f"\n{Y}Setup cancelled.{RST}")
            sys.exit(0)
