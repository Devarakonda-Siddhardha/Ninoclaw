def choose(prompt, options, default_idx=0):
    """Show a numbered menu and return chosen value"""
    print(f"\n  {W}{prompt}{RST}")
    for i, (label, _) in enumerate(options):
        marker = f"{G}▶{RST}" if i == default_idx else " "
